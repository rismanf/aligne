                    <h1>
                        Supporting Local Entrepreneurs to Go Global,
                        Telkom Through NeutraDC Provides AI Technology
                        Training for SMEs in Mandalika
                    </h1>
                </div>

                <div class="news__row row g-4">
                    <div class="col-12 news__content">
                        <figure class="figure news__content--featured">
                            <img src="/assets/img/news-1.webp" class="figure-img img-fluid" alt="Img" />
                            <figcaption class="figure-caption">
                                Participants of Lombok SMEs Attending
                                the Training “Utilizing AI Technology
                                for SMEs to Go Global” Organized by
                                Telkom Indonesia through NeutraDC at
                                Bale 360, Mandalika, Lombok, NTB, on
                                Monday, October 1, 2024.
                            </figcaption>
                        </figure>
                        <div class="news__content--body">
                            <p>
                                Lombok, October 1, 2024 – Telkom
                                Indonesia’s subsidiary, NeutraDC, held a
                                training session titled “Utilizing AI
                                Technology for SMEs Towards Going
                                Global,” conducted following the
                                international MotoGP event in Mandalika
                                at Bale360, Mandalika, Lombok, West Nusa
                                Tenggara, on Monday, October 1, 2024.
                                This training aims to empower local
                                entrepreneurs to compete in the global
                                market, particularly for local
                                businesses in Mandalika or Lombok, which
                                has the potential to host international
                                events frequently.
                            </p>
                            <p>
                                NeutraDC’s CEO, Andreuw Th.A.F, stated
                                that Lombok's status prompted this
                                initiative as a favorite tourist
                                destination that often hosts
                                international events like the MotoGP. He
                                noted that over 100,000 local
                                entrepreneurs in Lombok need to
                                continually enhance their skills to
                                remain competitive on a global scale.
                                “Mandalika has an international circuit
                                that attracts many global events. This
                                is a significant opportunity for SMEs to
                                leverage technology to adapt their
                                businesses to the demands of the
                                international market,” Andreuw remarked
                                separately.
                            </p>
                            <p>
                                The VP of Corporate Communication at
                                Telkom Indonesia, Andri Herawan Sasoko,
                                indicated that the training was
                                conducted to showcase the company’s
                                dedication to sharing up-to-date
                                information about technology that local
                                business players can utilize, especially
                                in seizing opportunities from
                                international events. "We found that
                                some SMEs in Lombok struggle to market
                                their products and reach a broader
                                audience, even though their products are
                                quite competitive. With this training,
                                we hope SMEs can better understand how
                                to promote their products," Andri said.
                            </p>
